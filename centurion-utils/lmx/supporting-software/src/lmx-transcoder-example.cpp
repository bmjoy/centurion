//------------------------------------------------------------------------------
//                   class c_external_transcoder_ascii_extended
//                           ASCII extensions setup
//------------------------------------------------------------------------------

c_external_transcoder_ascii_extended::c_external_transcoder_ascii_extended(
                                c_read & r_reader_in,
                                c_error & r_error_in,
                                const char * const cp_extended_table_in[] )
    :
    c_external_transcoder(
                    r_reader_in,
                    r_error_in ),
    mpp_extended_table( cp_extended_table_in )
{
}

c_external_transcoder::s_result c_external_transcoder_ascii_extended::read()
{
    int c = mr_reader.read();

    if( c == c_read::k_eof )
        return s_result( c_read::k_eof );

    if( c <= 0x7f )
        return s_result( c );

    const char * const p_utf8_expansion = mpp_extended_table[ c - 0x80 ];

    return s_result( static_cast<unsigned char>( *p_utf8_expansion ), p_utf8_expansion + 1 );
}

