extern const char * const kc_utf_8_from_iso_8859_15[];
extern const char * const kc_utf_8_from_iso_8859_16[];
extern const char * const kc_utf_8_from_iso_8859_2[];
extern const char * const kc_utf_8_from_iso_8859_3[];
extern const char * const kc_utf_8_from_iso_8859_4[];

struct s_transcoder_name_to_table_mappings
{
    const char * p_name;
    const char * const * const cp_transcoder_table;
} transcoder_name_to_table_mappings[] = {
        { "ISO-8859-2", kc_utf_8_from_iso_8859_2 },
        { "ISO-8859-3", kc_utf_8_from_iso_8859_3 },
        { "ISO-8859-4", kc_utf_8_from_iso_8859_4 },
        { LMXNULL, LMXNULL }
    };

c_external_transcoder * c_external_transcoder_factory_example::get_transcoder(
            c_read & r_reader_in,
            c_error & r_error_in,
            c_get_as_utf8::e_encoding_mode initial_encoding_mode_in,
            const char * p_encoding_name_in ) const
{
    if( initial_encoding_mode_in != c_get_as_utf8::EXR_UTF8 )
        return LMXNULL; // All of the below encodings require us to have initailly assumed a UTF-8 encoding (as opposed to UTF-16 etc.)

    // If the transcoder has special properties, define it's own handling class
    if( xstricmp( p_encoding_name_in, "windows-1252" ) == 0 ||
                xstricmp( p_encoding_name_in, "cp-1252" ) == 0 )
        return new c_external_transcoder_win_1252( r_reader_in, r_error_in );

    // Some encodings can use a common base class for handling the transcoding
    if( xstricmp( p_encoding_name_in, "ISO-8859-15" ) == 0 )
        return new c_external_transcoder_ascii_extended( r_reader_in, r_error_in, kc_utf_8_from_iso_8859_15 );

    if( xstricmp( p_encoding_name_in, "ISO-8859-16" ) == 0 )
        return new c_external_transcoder_ascii_extended( r_reader_in, r_error_in, kc_utf_8_from_iso_8859_16 );

    // ...so much so that they can be expressed via tables
    for( s_transcoder_name_to_table_mappings * p_transcoder_name_to_table_mappings = transcoder_name_to_table_mappings;
            p_transcoder_name_to_table_mappings->p_name != LMXNULL;
            ++p_transcoder_name_to_table_mappings )
    {
        if( xstricmp( p_encoding_name_in, p_transcoder_name_to_table_mappings->p_name ) == 0 )
            return new c_external_transcoder_ascii_extended( r_reader_in, r_error_in, p_transcoder_name_to_table_mappings->cp_transcoder_table );
    }

    return LMXNULL;
}

